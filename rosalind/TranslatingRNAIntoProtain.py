def readInput():
    dir = os.path.dirname(__file__)
    file = os.path.join(dir, 'rosalind.txt')
    with open(file, "r") as file:
        return file.readline()




def solve():
    seq = readInput()
    idx = 0
    ans = ''
    while idx < len(seq)-3:
        codon = seq[idx:idx+3]
        if codon_table[codon]=='Stop':
            return ans
        if codon not in codon_table:
            return ans 
        ans += codon_table[codon]
        idx += 3
    print(idx,len(seq))
    return ans


